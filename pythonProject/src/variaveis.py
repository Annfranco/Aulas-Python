#Variavel em Python

numero = 10
numero2 = 5

print(f"Aritméticos")
#soma
resultado = numero + numero2
print(f"Soma: {resultado}")

#subtração
print(f"Subtração: {numero - numero2}")

#multiplicação
resultado = numero * numero2
print(f"Multiplicação: {resultado}")

#divisão
print(f"Divisão: {numero / numero2}")

#divisão aó inteiro
resultado = numero // numero2
print(f"Divisão inteira: {resultado}")

#módulo (retornando o resto de uma divisão)
resultado = numero % numero2
print(f"Módulo: {resultado}")